
// Individually selecting
// Using different symbols, we can separately select the different parts in non-Array data as well
let x = "Are ki"
console.log(x.split(""));

let y = "Go-od-bye!"
console.log(y.split("-"));

let w = "A#ki#b Hasan"
console.log(w.split("#"));

let v = "Akib Hasan"
console.log(v.toLocaleUpperCase());

let u = "Akib Hasan"
console.log(u.toLocaleLowerCase());